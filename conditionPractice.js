var grade = 75;

if(90 <= grade && grade <= 100){
    console.log("You got an A!");
}else if(80 <= grade && grade <= 89){
    console.log("You got a B.");
}else if(70 <= grade && grade <= 79){
    console.log("You got a C.");
}else if(69 <= grade && grade <= 60){
    console.log("You got a C.");
}else{
    console.log("You failed.");
}